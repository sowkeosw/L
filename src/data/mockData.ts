const generateComments = (comments: string[]) => {
  const count = comments.length;
  return comments.map((content, i) => {
    // i가 0일 때(익명1) 가장 오래된 시간, i가 마지막일 때 '방금 전'
    const minutesAgo = (count - 1 - i) * 3; 
    
    let timeString = '';
    if (minutesAgo === 0) {
      timeString = '방금 전';
    } else if (minutesAgo < 60) {
      timeString = `${minutesAgo}분 전`;
    } else {
      const hours = Math.floor(minutesAgo / 60);
      const mins = minutesAgo % 60;
      timeString = mins > 0 ? `${hours}시간 ${mins}분 전` : `${hours}시간 전`;
    }

    return {
      id: i + 1,
      author: `익명${i + 1}`,
      time: timeString,
      content
    };
  });
};

